#pragma once
//===============================================
// DamageTags.h
//  攻撃の“種別”タグ。ガード相性や演出分岐に使用。
//  必要なら新しいタグを増やしていく（例：Fire, Ice 等）。
//===============================================
enum class DamageTag {
    Normal,         // 既定：特に指定なし（汎用）
    MeleeLight,     // 近接・軽攻撃
    MeleeHeavy,     // 近接・重攻撃
    ProjectileLob,  // 放物線の投擲（弾）
    Beam,           // 直線ビーム/魔法
    Explosion       // 爆発ダメージ（ボスの弱点）
};
